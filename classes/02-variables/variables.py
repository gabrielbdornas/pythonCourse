# Variáveis são pequenas 'Caixas' aonde guardamos informações ao longo da execução do nosso código
# variavel_name = variavel_value (utilizar letras minúsculas e "_" para separar nomes na criação de variável - snake_lower_case)
# python é case sensitive, diferenças entre letras maiúsculas e minúsculas
name = 'Gabriel' # variável criada e atribuida a uma string
print(name)

price_1 = 10 # Número sem pontos são inteiros
print(price_1)

price_2 = 10.5 # Número com pontos são floats
print(price_2)

price_3 = 10
price_3 = 15
print(price_3) # printará a última atribuição na variável, neste caso price_3 será 15

is_published = True # variável criada e atribuida a um boolean (True ou False - primeira letra maiúscula)
print(is_published)

# Concatenação - outra expressão
# concatenação exige que strings
print("Olá " + name)

