# for loops
print("Iterando sobre as letras da linguagem de programação que estamos aprendendo")
for letra in "Python":
    print(letra)
print("Fim da iterando sobre as letras da linguagem de programação que estamos aprendendo")

print("Iterando sobre minha lista de compras")
for item in ['Banana', 'Maçã', 'Abacaxi']:
    print(item)
print("Fim da iteração sobre minha lista de compras")

print("Iterando sobre uma lista de números utilizando a função range")
for item in range(10):
    print(item)
print("Fim da iteração sobre uma lista de números utilizando a função range")

